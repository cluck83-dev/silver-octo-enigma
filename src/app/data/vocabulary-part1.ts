export function getLessons(): string[] {
  const lessons = new Set(vocabularyDataPart1.map(v => v.lesson));
  return Array.from(lessons).sort((a, b) => {
    const getValue = (lesson) => {
      if (lesson.startsWith('Einheit I')) return 1;
      if (lesson.startsWith('Einheit II')) return 2;
      if (lesson.startsWith('Einheit III')) return 3;
      if (lesson.startsWith('Einheit IV')) return 4;
      if (lesson.startsWith('Einheit V')) return 5;
      if (lesson.startsWith('Einheit VI')) return 6;
      if (lesson.startsWith('Einheit VII')) return 7;
      if (lesson === 'Auszeit א') return 8;
      if (lesson.startsWith('Lektion')) {
        const num = parseInt(lesson.replace('Lektion ', ''));
        if (num <= 6) return 8 + num;
        if (num >= 7) return 16 + num - 7;
      }
      if (lesson === 'Auszeit ב') return 15;
      return 1000;
    };
    return getValue(a) - getValue(b);
  });
}

export function getWordTypes(): string[] {
  const order = ["Substantive", "Adjektive", "Verben", "Pronomen", "Fragewort", "Zahlen", "Andere"];
  const types = new Set(vocabularyDataPart1.map(v => v.wordType));
  const typeArray = Array.from(types);
  return typeArray.sort((a, b) => {
    const indexA = order.indexOf(a);
    const indexB = order.indexOf(b);
    if (indexA === -1 && indexB === -1) return a.localeCompare(b);
    if (indexA === -1) return 1;
    if (indexB === -1) return -1;
    return indexA - indexB;
  });
}

export function filterByLesson(lesson: string): Vocabulary[] {
  return vocabularyDataPart1.filter(v => v.lesson === lesson);
}

export function filterByWordType(wordType: string): Vocabulary[] {
  return vocabularyDataPart1.filter(v => v.wordType === wordType);
}
